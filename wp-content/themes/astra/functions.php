function enqueue_custom_js() {
  wp_enqueue_script( 'custom-script', get_template_directory_uri() . '/assets/js/custom-user-tracking.js',array('jquery'), '3.6.0', true);
  wp_localize_script('custom-script', 'ajax_object', array('ajax_url' => admin_url('admin-ajax.php')));
}

add_action( 'wp_enqueue_scripts', 'enqueue_custom_js' );


function custom_track_user_journey() {
  try {
    // Retrieve the tracking data from the AJAX request
    $tracking_data = $_POST['trackingData'];
    
    // Decode the JSON string into an associative array
    $tracking_data = stripslashes($_POST['trackingData']);
    $tracking_data = urldecode($tracking_data); 
    $tracking_data = json_decode($tracking_data, true);
    if ($tracking_data === null) {
      $error_code = json_last_error();
      $error_message = json_last_error_msg();
      // Log or display the error information for debugging
      die('JSON decoding error: '.$_POST['trackingData'] . $error_message . ' (Error code: ' . $error_code . ')');
  }

    // Get the individual tracking data fields
    $userID = @$tracking_data['userID'];
    $sessionStartTime = @$tracking_data['sessionStartTime'];
    $sessionEndTime = @$tracking_data['sessionEndTime'];
    $referrerURL = @$tracking_data['referrerURL'];
    $pageVisits = json_encode($tracking_data['pageVisits']);
    $visitCount = @$tracking_data['visitCount'];
    $eventClicked = @$tracking_data['eventClicked'];

    // Save the tracking data to the database
    global $wpdb;
    $table_name = $wpdb->prefix . 'user_tracking';

    $wpdb->insert(
      $table_name,
      array(
        'user_id' => $userID,
        'session_start_time' => $sessionStartTime,
        'session_end_time' => $sessionEndTime,
        'referrer_url' => $referrerURL,
        'visit_count' => $visitCount,
        'page_visits' => $pageVisits,
        'eventClicked' => $eventClicked
      ),
      array(
        '%s',
        '%s',
        '%s',
        '%s',
        '%d',
        '%s',
        '%s'
      )
    );

    // Send a response back to the JavaScript code
    wp_send_json_success('Tracking data received and saved successfully '.json_encode($tracking_data));
  }
  catch(Exception $ex) {
    wp_send_json_error($ex->getMessage());
  }
  
}

add_action( 'wp_ajax_custom_track_user_journey', 'custom_track_user_journey' );
add_action( 'wp_ajax_nopriv_custom_track_user_journey', 'custom_track_user_journey' );


